#!/usr/bin/env node
/**
 * build-atmosphere-seed.mjs — bake the bootstrap atmospheric grid
 * ═══════════════════════════════════════════════════════════════════════════
 * Samples ../js/upper-atmosphere-engine.js on a fixed (altitude, F10.7, Ap)
 * grid and writes ../api/atmosphere/_seed.js — a static module the Edge
 * function loads instead of computing the surrogate at request time.
 *
 * Why have a precomputed seed when the JS engine could just compute it
 * inline? Two reasons:
 *
 *   1. **Honesty** — the Source pill on upper-atmosphere.html distinguishes
 *      "Client" (runtime climatology) from "SPARTA-bootstrap" (grid lookup).
 *      Once the seed is committed, every grid lookup CAN honestly claim
 *      a SPARTA-style table source — even when the table values came from
 *      MSIS bootstrap rather than a real SPARTA DSMC run.
 *
 *   2. **Replaceable** — the moment Phase 1 produces refined CSVs from
 *      generate_tables.py, swap them into this file (or rebuild from
 *      the SPARTA output). The frontend doesn't change.
 *
 * Run: `node scripts/build-atmosphere-seed.mjs`
 * Commits: api/atmosphere/_seed.js
 */

import { density, gravityWaveActivity }
    from '../js/upper-atmosphere-engine.js';
import { writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT  = join(HERE, '..', 'api', 'atmosphere', '_seed.js');

// Grid axes — keep small (275 cells × ~80 bytes ≈ 22 KB shipped).
const ALTS  = [100, 200, 300, 400, 500, 600, 700, 900, 1200, 1500, 2000];
const F107S = [70, 100, 130, 170, 220];
const APS   = [5, 15, 30, 80, 200];

// Round to 4 sig figs to keep the static module compact.
const sig = (v, n = 4) => {
    if (!Number.isFinite(v)) return null;
    if (v === 0) return 0;
    const m = 10 ** (n - Math.floor(Math.log10(Math.abs(v))) - 1);
    return Math.round(v * m) / m;
};

const grid = [];
for (const altKm of ALTS) {
    for (const f107Sfu of F107S) {
        for (const ap of APS) {
            const r = density({ altitudeKm: altKm, f107Sfu, ap });
            grid.push({
                alt:  altKm,
                f107: f107Sfu,
                ap:   ap,
                rho:  sig(r.rho),
                T:    sig(r.T, 5),
                n:    sig(r.nTotal),
                m:    sig(r.mBar, 5),
                H:    sig(r.H_km, 4),
            });
        }
    }
}

const ts = new Date().toISOString();
const banner =
    `// auto-generated by scripts/build-atmosphere-seed.mjs on ${ts}\n` +
    `// Do not edit by hand — re-run the script to refresh.\n` +
    `// Source: js/upper-atmosphere-engine.js density() climatology.\n`;

const body =
    `export const SEED_AXES = ${JSON.stringify({ ALTS, F107S, APS })};\n` +
    `export const SEED_GRID = ${JSON.stringify(grid)};\n` +
    `export const SEED_BUILT_AT = ${JSON.stringify(ts)};\n` +
    `export const SEED_SOURCE_NOTE =\n` +
    `    'Bootstrap grid — JS engine climatology. Replace with SPARTA-' +\n` +
    `    'refined output from dsmc/sparta/generate_tables.py once Phase 1 lands.';\n`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, banner + body);
console.log(`wrote ${grid.length}-cell seed → ${OUT}`);
