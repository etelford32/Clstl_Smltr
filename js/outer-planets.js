/**
 * outer-planets.js — VSOP87D truncated series for Jupiter, Saturn, Uranus, Neptune
 *
 * Theory: Bretagnon & Francou (1988), A&A Suppl. 82, 279–304.
 * Coordinates: heliocentric ecliptic longitude L, latitude B, radius R
 *   in the mean dynamical ecliptic and equinox of J2000.0.
 *
 * Series evaluation:
 *   V = Σ_n [ τ^n × Σ_k A_k × cos(B_k + C_k × τ) ]
 *   τ = (JD − 2451545.0) / 365250.0   (Julian millennia since J2000.0)
 *
 * Coefficient units: A in 1e-8 rad (L, B) or 1e-8 AU (R); C in rad/millennium.
 *
 * Accuracy (truncated series, 1800–2200):
 *   Jupiter: ~0.05°  Saturn: ~0.05°  Uranus: ~0.1°  Neptune: ~0.1°
 */

const _D2R = Math.PI / 180;
const _R2D = 180 / Math.PI;
const _jdNow = () => Date.now() / 86400000 + 2440587.5;

function _evalSeries(terms, tau) {
    let s = 0;
    for (const [A, B, C] of terms) s += A * Math.cos(B + C * tau);
    return s;
}

