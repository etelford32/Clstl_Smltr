// ─────────────────────────────────────────────────────────────────────────────
// Core VSOP87D evaluators
// ─────────────────────────────────────────────────────────────────────────────

function _vsop87(jd, L0, L1, L2, B0, R0, R1) {
    const tau  = (jd - 2451545.0) / 365250.0;
    const tau2 = tau * tau;

    const L_raw = _evalSeries(L0, tau)
                + _evalSeries(L1, tau) * tau
                + _evalSeries(L2, tau) * tau2;
    const B_raw = _evalSeries(B0, tau);
    const R_raw = _evalSeries(R0, tau)
                + _evalSeries(R1, tau) * tau;

    let L_rad = (L_raw * 1e-8) % (2 * Math.PI);
    if (L_rad < 0) L_rad += 2 * Math.PI;

    const B_rad = B_raw * 1e-8;
    const R_AU  = R_raw * 1e-8;
    const cosB  = Math.cos(B_rad);

    return {
        L_rad, B_rad, R_AU,
        x_AU: R_AU * cosB * Math.cos(L_rad),
        y_AU: R_AU * cosB * Math.sin(L_rad),
        z_AU: R_AU * Math.sin(B_rad),
    };
}

/** Convert raw VSOP output to the horizons.js-compatible position record. */
function _toPos(v) {
    const lon = ((v.L_rad * _R2D) % 360 + 360) % 360;
    return {
        lon_rad:  v.L_rad,
        lat_rad:  v.B_rad,
        dist_AU:  v.R_AU,
        x_AU:     v.x_AU,
        y_AU:     v.y_AU,
        z_AU:     v.z_AU,
        lon,
        source:   'vsop87',
    };
}

// ─────────────────────────────────────────────────────────────────────────────
// Public API
// ─────────────────────────────────────────────────────────────────────────────

export function vsop87Jupiter(jd = _jdNow()) {
    return _vsop87(jd, JUP_L0, JUP_L1, JUP_L2, JUP_B0, JUP_R0, JUP_R1);
}
export function vsop87Saturn(jd = _jdNow()) {
    return _vsop87(jd, SAT_L0, SAT_L1, SAT_L2, SAT_B0, SAT_R0, SAT_R1);
}
export function vsop87Uranus(jd = _jdNow()) {
    return _vsop87(jd, URA_L0, URA_L1, [], URA_B0, URA_R0, URA_R1);
}
export function vsop87Neptune(jd = _jdNow()) {
    return _vsop87(jd, NEP_L0, NEP_L1, [], NEP_B0, NEP_R0, NEP_R1);
}

/** Jupiter heliocentric ecliptic position — horizons.js-compatible. */
export function jupiterVSOP(jd = _jdNow()) { return _toPos(vsop87Jupiter(jd)); }
/** Saturn heliocentric ecliptic position — horizons.js-compatible. */
export function saturnVSOP(jd = _jdNow())  { return _toPos(vsop87Saturn(jd));  }
/** Uranus heliocentric ecliptic position — horizons.js-compatible. */
export function uranusVSOP(jd = _jdNow())  { return _toPos(vsop87Uranus(jd));  }
/** Neptune heliocentric ecliptic position — horizons.js-compatible. */
export function neptuneVSOP(jd = _jdNow()) { return _toPos(vsop87Neptune(jd)); }
